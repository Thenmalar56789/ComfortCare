def get_lumi_reply(user_input):
    if "sad" in user_input.lower():
        return "I'm here for you. You are not alone. 🌸"
    elif "happy" in user_input.lower():
        return "Yay! I'm so glad to hear that! 😊"
    elif "nervous" in user_input.lower():
        return "Take a deep breath. You're doing better than you think. 💖"
    elif "love" in user_input.lower():
        return "Love is the most beautiful feeling. You're deeply loved. 💞"
    else:
        return "I'm always here to listen and support you 💫"